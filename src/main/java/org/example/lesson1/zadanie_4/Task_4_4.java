package org.example.lesson1.zadanie_4;

public class Task_4_4 {

    public static void main(String[] args) {

        for(int i = 100; i >= 50; i--){
            System.out.print(i + " ");
        }

        System.out.println("\n");
        int j = 100;
        while(j >= 50){
            System.out.print(j + " ");
            j--;
        }

    }
}
